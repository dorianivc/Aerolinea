    @Override
    public void update(Observable o, Object arg) {
        codigo.setText(model.usuario.getUsuario());
        Nombre.setText(model.usuario.getNombre());
        Apellidos.setText(model.usuario.getApellidos());
        Contraseña.setText(model.usuario.getContrasena());
       Correo.setText(model.usuario.getCorreoElectronico());
        Direccion.setText(model.usuario.getDireccion());
        Telefono.setText(model.usuario.getTelefono());
        Celular.setText(model.usuario.getCelular());
        String dia = String.valueOf(model.usuario.getFechaNacimiento());
        String x;
        x = dia.substring(8, 10);
        Day.setText(x);
        int mes =model.usuario.getFechaNacimiento().getMonth(); mes++;
        Mes.setText(String.valueOf(mes));
        int year = model.usuario.getFechaNacimiento().getYear(); year +=1900;
        año.setText(String.valueOf(year));
    }



